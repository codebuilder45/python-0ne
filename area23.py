# this program will take the length and width of a rectangle to calculate the area
# and display the answer.
length_rec = 4
width_rec = 9
area = length_rec * width_rec


print("\tThe length is: ", "\t\b\b\b\b\b" + str(length_rec))
print("\tThe width is: ", "\t   " + str(width_rec))
print("\n\tThe area of the rectangle with a length of",
      str(length_rec), "and a width of", str(width_rec), "is", str(area), end=".")
